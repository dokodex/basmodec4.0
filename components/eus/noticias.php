        <h2 class="noticias__noticia__title">''Como preparar una habitación antes de pintar''</h2>
        <div class="noticias__notica__divimg"><img class="noticias__noticia__img" src="img/noticias/noticia1.jpg" alt=""/></div>
      </div>
      <div class="noticias_noticia__text noticias__noticia1">
        Antes de empezar a pintar la habitación:<br><br>
        1. Proteger el espacio para evitar manchar. Para eso lo que necesitamos es:<br>
        <p class="noticias__noticia__text__tab">
          ·Cinta de carrocero para proteger interruptores, enchufes, zócalos…<br>
          ·Plástico para cubrir muebles y cosas que queramos proteger.<br>
          ·Papel o papel con cinta para cubrir el suelo u otras zonas. <br>
        </p>
        2. Prepara la superficie a pintar.<br><br>
        Rellenaremos las grietas que pueda haber en las paredes o techos con masilla extendiéndola con una espátula.<br><br>
        Debemos dejar secar la masilla y pasar una lija para que quede la superficie quede lisa y lista para pintar. Después pasamos un paño para quitar el polvo que haya quedado y ya podemos comenzar a pintar!
      </div>
    </div>
    <div class="noticias__noticia noticias__noticia2">
      <div class="noticias__noticia__grid">
        <h2 class="noticias__noticia__title">''Paso a paso para pintar una habitación''</h2>
        <div class="noticias__notica__divimg"><img class="noticias__noticia__img" src="img/noticias/noticia2.jpg" alt=""/></div>
      </div>
      <div class="noticias_noticia__text">
        MATERIAL: Protectores (papel, cinta, plástico), lija; imprimación, pintura, rodillo, brocha, cubeta; alargador.<br><br>
        <p class="noticias__noticia__text__tab">
          1. Con la superficie preparada lo mejor es empezar con una imprimación para preparar la superficie que queremos pintar. Es una primera capa que hace que la pintura se adhiera mejor al soporte en caso de que este sea nuevo, si ya está pintado no hace falta. 
        </p>
        <p class="noticias__noticia__text__tab">
          2. Con la cinta se pueden hacer las divisiones en las paredes y techos para que la separación entre colores quede lineal. Consejo: no cortar la cinta. ¡Haced una línea entera en toda la pared!
        </p>
        <p class="noticias__noticia__text__tab">
          3. Podemos comenzar a pintar. Usaremos una brocha para recortar esquinas. 
        </p>
        <p class="noticias__noticia__text__tab">
          4. Seguir pintando con el rodillo. Consejo: En los dos sentidos, vertical y horizontal.
        </p>
        <p class="noticias__noticia__text__tab">
          5. Daremos una segunda mano para un mejor resultado, respetando el tiempo de secado que propone cada fabricante. (dependiendo de cada tipo de pintura, consultad en tiendas Basmodec).
        </p>
        <p class="noticias__noticia__text__tab">
          6. Retiramos las cintas antes de que seque del todo la pintura.
        </p>
        <p class="noticias__noticia__text__tab">
          7. Atentos a las manchas para poder limpiarlas antes de que la pintura se seque, será mucho más fácil.
        </p>
        <p class="noticias__noticia__text__tab">
          8. El material se puede limpiar fácilmente con agua (la mayoría de pinturas son al agua). 
        </p>
      </div>
